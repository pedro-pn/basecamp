int	ft_is_prime(int nb)
{
	int	p;

	if (nb <= 1)
		return (0);
	p = 2;
	while (p <= nb / p)
	{
		if (nb % p == 0)
		{
			return (0);
		}
		p++;
	}
	return (1);
}
